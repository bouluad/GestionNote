package com.gestion.note.bo;

public class parametre {
	
	int anneeuniv ;
	int semestre ;
	int seuilCp ;
	int seuilCi ;
	
	
	public parametre() {
		super();
	}
	public int getAnneeuniv() {
		return anneeuniv;
	}
	public void setAnneeuniv(int anneeuniv) {
		this.anneeuniv = anneeuniv;
	}
	public int getSemestre() {
		return semestre;
	}
	public void setSemestre(int semestre) {
		this.semestre = semestre;
	}
	public int getSeuilCp() {
		return seuilCp;
	}
	public void setSeuilCp(int seuilCp) {
		this.seuilCp = seuilCp;
	}
	public int getSeuilCi() {
		return seuilCi;
	}
	public void setSeuilCi(int seuilCi) {
		this.seuilCi = seuilCi;
	}
	
	

}
